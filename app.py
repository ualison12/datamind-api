# Operadores Aristiméticos

a = 5
b = 3
 
# soma 
print("Soma:", a + b) # Saída:8

# Subtração
print("Subtração:", a - b) # Saída:2

# Multiplicação
print("Multiplicação:", a * b) # Saída:15

# Divisão
print("Divisão:", a / b) # Saída:1.66666...

# Divisão inteira
print("Divisão inteira:", a // b) # Saída:1

# Módulo (resto da divisão)
print("Módulo:", a % b) # Saída:2

# Exponenciação
print("Exponenciação:", a ** b) # Saída: